package modelo;

public class Restaurantes {
	
	private int idRestaurante;
	private String nome;
	private String pontoReferencia;
	private String tipoCulinaria;
	private String tipoRestaurante;
	private String faixaPrecos;
	private String horarioAbertura;
	private String horarioEncerramento;
	public int getIdRestaurante() {
		return idRestaurante;
	}
	public void setIdRestaurante(int idRestaurante) {
		this.idRestaurante = idRestaurante;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getPontoReferencia() {
		return pontoReferencia;
	}
	public void setPontoReferencia(String pontoReferencia) {
		this.pontoReferencia = pontoReferencia;
	}
	public String getTipoCulinaria() {
		return tipoCulinaria;
	}
	public void setTipoCulinaria(String tipoCulinaria) {
		this.tipoCulinaria = tipoCulinaria;
	}
	public String getTipoRestaurante() {
		return tipoRestaurante;
	}
	public void setTipoRestaurante(String tipoRestaurante) {
		this.tipoRestaurante = tipoRestaurante;
	}
	public String getFaixaPrecos() {
		return faixaPrecos;
	}
	public void setFaixaPrecos(String faixaPrecos) {
		this.faixaPrecos = faixaPrecos;
	}
	public String getHorarioAbertura() {
		return horarioAbertura;
	}
	public void setHorarioAbertura(String horarioAbertura) {
		this.horarioAbertura = horarioAbertura;
	}
	public String getHorarioEncerramento() {
		return horarioEncerramento;
	}
	public void setHorarioEncerramento(String horarioEncerramento) {
		this.horarioEncerramento = horarioEncerramento;
	}
	
	
}
