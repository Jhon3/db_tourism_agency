package modelo;

public class Park {
	
	private int idPark;
	private String nome;
	private Local_ local_;
	public int getIdPark() {
		return idPark;
	}
	public void setIdPark(int idPark) {
		this.idPark = idPark;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public Local_ getLocal_() {
		return local_;
	}
	public void setLocal_(Local_ local_) {
		this.local_ = local_;
	}
	
	

}
