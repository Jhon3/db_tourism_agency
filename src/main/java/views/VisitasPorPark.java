package views;

public class VisitasPorPark {
	
	private String nome;
	private String cidade;
	private String pais;
	private String moeda;
	private int pacotes_comprados;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getCidade() {
		return cidade;
	}
	public void setCidade(String cidade) {
		this.cidade = cidade;
	}
	public String getPais() {
		return pais;
	}
	public void setPais(String pais) {
		this.pais = pais;
	}
	public String getMoeda() {
		return moeda;
	}
	public void setMoeda(String moeda) {
		this.moeda = moeda;
	}
	public int getPacotes_comprados() {
		return pacotes_comprados;
	}
	public void setPacotes_comprados(int pacotes_comprados) {
		this.pacotes_comprados = pacotes_comprados;
	}
		
}
